void main(){ 
	#ifdef MCU_STC12
	P3M1 &= ~(1<<2),	P3M0 |=  (1<<2);	//P3.2 set as push-pull output mode
  #endif
	lcd_initial();
	bl=1;
	delayms(500);
	uart_init(9600);
	dsp_single_colour(WHITE);//白色
	line_color=color_array[color_pos];
	color_name_fn();
	delay_ms(200);//200ms显示的时间
  cls(10,115,50,13);//清理小区域屏幕
	width_fn(line_width_now);
	cls(5,115,65,13);
	length_fn(line_length);
	cls(5,115,70,13);
  while(1){
		Read_PS2(); 
		if(out[3]==0xEF){//左边上键
			line_up(line_length,line_width_now,line_color);
		}
		if(out[3]==0xBF){//左边下键
			line_down(line_length,line_width_now,line_color);
		}
		if(out[3]==0x7F){//左边左键
			out_oled();
			line_left(line_length,line_width_now,line_color);
		}
		if(out[3]==0xDF){//左边右键
			out_oled();
			line_right(line_length,line_width_now,line_color);
		}
		if(out[4]==0xEF && one==0){//右边上键  换颜色
		 one=1;
		 color_pos+=1;
		 if(color_pos>=8)color_pos=8;
		 line_color=color_array[color_pos];
		 color_name_fn();
		 delay_ms(200);
		 cls(10,115,50,13);
		}
		if(out[4]==0xBF && one==0){//右边下键  换颜色
			one=1;
			color_pos-=1;
			if(color_pos<=0)color_pos=0;
			line_color=color_array[color_pos];
			color_name_fn();
			delay_ms(200);
			cls(10,115,50,13);
		}
		if(out[4]==0xDF && one==0){//清屏
			one=1;
			Display_ASCII8X16(10,115,"clear screen");
			dsp_single_colour(WHITE);//白色
		}
		if(out[4]==0xF7 && one==0){//R1键  线宽度+1
			one=1;
			line_width_now+=1;
			if(line_width_now>=5)line_width_now=5;
			width_fn(line_width_now);
			cls(5,115,65,13);
		}
		if(out[4]==0xFD && one==0){//R2键   线宽度-1
			one=1;
			line_width_now-=1;
			if(line_width_now<=1)line_width_now=1;
			width_fn(line_width_now);
			cls(5,115,65,13);
		}
		if(out[4]==0xFB && one==0){
			one=1;
			line_length+=3;
			if(line_length>=8)line_length=8;
			length_fn(line_length);
			cls(5,115,70,13);
		}
		if(out[4]==0xFE && one==0){
			one=1;
			line_length-=3;
			if(line_length<=2)line_length=2;
			length_fn(line_length);
			cls(5,115,70,13);
		}
		if(out[4]==0xFF)one=0;
		 delayms(70);
	}
} 

